// Lookup helpers used by app.js
window.LEGAL_DB_HELPERS = {
  // Normalize doc id by stripping accents/spacing variants
  normalizeId(id) {
    return String(id || "")
      .toUpperCase()
      .replace(/Đ/g, "D")
      .replace(/\s+/g, "")
      .replace(/-/g, "-");
  },
  findDoc(rawId) {
    const norm = this.normalizeId(rawId);
    for (const key of Object.keys(window.LEGAL_DB)) {
      if (this.normalizeId(key) === norm) return window.LEGAL_DB[key];
    }
    return null;
  },
  findArticle(doc, articleNumber) {
    if (!doc) return null;
    const target = String(articleNumber).trim();
    for (const ch of doc.chapters || []) {
      for (const a of ch.articles) {
        const num = a.number.replace(/Điều\s*/i, "").trim();
        if (num === target) return a;
      }
    }
    return null;
  }
};
